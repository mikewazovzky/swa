<h2>Рекомендации</h2>

<p>	Самое сильно впечатление в парке производят сами деревья, их много, они великолепны и, в общем то, похожи друг на друга. 
	Большого смысла посещать все официальные площадки со знаменитыми деревья нет. Планируя поездку в парк я искал инфорамацию 
	по наиболее популярным точкам, и планировал свой маршрут в привязке к ним. Сейчас, в  условиях ограниченного времени, 
	я бы выбрал более дальние пешые прогулки по лесным тропам, пожертвовав количеством осмотреных точек. Вся информация по 
	маршрутам в парке есть на сайте.</p>
	
<p>	В качестве места для прогулки я бы выбрал окрестности Лоджпула (<strong>Lodgepole</strong>) в надежде все таки  
	сфотографировать медведя.</p>
	
<p>	Парк оказался не очень удачным для фотографирования. Сочетание яркого калифорнийского солнца и глубоких лесных теней делает 
	снимки очень контрастными. Имет смысл заранее подумать/узнать, как фотографировать в таких условиях. В лесу достаточно рано 
	начинает смеркаться, освещенность быстро падает.</p>
	
