<p>	Реликтовые леса гигантских деревьев – секвой покрывают склоны гор в северной части Калифорнии. Посмотреть на них можно в 
	парке Секвой (<strong>Sequoia National Park</strong>) или расположенных севернее Секвойя Нешенал Форест (<strong>Sequoia 
	National Forest</strong>) и парке Йосимити (<strong>Yosemite</strong>). Деревья совершенно замечательные, они огромные, 
	симпатичного красноватого цвета, покрыты толстым слоем приятной на ощупь пружинящей коры.</p>
	
<p>	Цель моего путешествия - Парк Секвой. Парк является объединенным, включает в себя помимо Парка Секвой (<strong>Sequoia 
	National Park</strong>) еще примыкающий с севера Королевский Каньон (<strong>King Canyon National Park</strong>).</p>

<p>	Так как основной целью моего путешествия были компактно (ну относительно компактно) расположенные парки Юты и Аризоны, 
	мне пришлось сделать очень приличную петлю ради того, чтобы посмотреть на секвои, ну и на океан тоже.  Проехать парк 
	насквозь с запада на восток не возможно – нет дороги.  Наиболее удачными вариантами маршрута кажутся такие:</p>

<ul style='list-style-type: square;'>
	<li>Совместить посещение парка Секвой с поездками по атлантическому побережью Сан-Франциско – Лос-Анджелес – Сан-Диего, </li>
	<li>Проехать от западного побережья на восток севернее,  через Йосимити (<strong>Yosemite, CA</strong>) и, затем, ехать в 
		сторону Лас Вегаса через Долину Смерти (<strong>Death Valley, NV</strong>).</li>
</ul>

<h2>Как добраться</h2>

<p>	Расстояние до парка от Сан-Франциско (<strong>San Francisco, CA</strong>) около 400 километров, от Лос-Анджелеса
	(<strong>Los Angeles, CA</strong>) 350 километров (я ехал из LA), от Лас-Вегаса (<strong>Las Vegas, NV</strong>) 640 
	километров (после посещения парка мой путь лежал в Вегас).</p>

<p>	Если есть желание провести в парке больше времени, необходимо остановиться недалеко от него. Ближайшие крупные населенные 
	пункты – Визелия (<strong>Visalia, CA</strong>) и Фресно (<strong>Fresno, CA</strong>), но можно найти отель и поближе к 
	парку, например в Три Риверс (<strong>Tree Rivers, CA</strong>). Места здесь совершенно замечательные, чем ближе к парку, 
	тем красивее и интереснее.</p>

<p>	К парку ведет 198 дорога от Визалии (<strong>Visalia, CA</strong>), расстояние до въезда в парк в  <strong>Foothill Visitor 
	Center</strong> 60 километров, и 180 дорога из Фресно (<strong>Fresno, CA</strong>). Дороги пересекаются в парке и кажется 
	вполне логичным проехать его с юга (въезд 198) на север (выезд по 180), как я и сделал, или наоборот.  Дорога 180 от 
	пересечения с 198 ведет вглубь парка (<strong>Kings Canyon</strong>) на 55 километров, возвращаться придется по ней же. 
	При планировании перемещений в парке нужно иметь ввиду, что сильно там не разгонишься, дороги достаточно извилистые  с 
	серьезным перепадом высот, скорость часто ограничена до 20-30-40 миль в час, стоят знаки "Осторожно! Wildlife crossing!"  
	с изображением различных представителей wildlife. В парке нет заправок, с учетом приличных расстояний имеет смысл посмотреть 
	на остаток в баке перед выездом из города.</p>

<p>	Карта парка из официального буклета</p>

