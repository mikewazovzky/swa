<p>	По территории парка курсируют шаттлы, но я не понял прелести перемещения на них, т.к. несмотря на пик туристического сезона 
	народу и автомобилей в парке было не много, на всех парковках было достаточно свободных мест. </p>

<h2>Туристический сезон</h2>

<p>	Парк находится в невысоких (6000 футов, 2000 метров) горах, в разгар июльской калифорнийской жары на подступах к парку, 
	здесь было очень комфортно. Думаю, что парк можно посещать в любое время года. Зимой здесь много снега, некоторые дороги 
	закрываются. На сайте парка всегда можно уточнить текущее состояние с погодой и дорогами. Ездить зимой по горным 
	серпантинам из-за снега небезопасно.</p>

